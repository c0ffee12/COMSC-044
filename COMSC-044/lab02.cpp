#include <iostream>

using namespace std;

int main()
{
	cout << "char is of size " << sizeof(char) << " bytes." << endl;
	cout << "short int is of size " << sizeof(short int) << " bytes." << endl;
	cout << "int is of size " << sizeof(int) << " bytes." << endl;
	cout << "long int is of size " << sizeof(long int) << " bytes." << endl;
	cout << "long long int is of size " << sizeof(long long int) << " bytes." << endl;
	cout << "float is of size " << sizeof(float) << " bytes." << endl;
	cout << "double is of size " << sizeof(double) << " bytes." << endl;
	cout << "long double is of size " << sizeof(long double) << " bytes." << endl;
	cin.get();
}
